# 1. Print a simple text message
print("Hello, World!")

# 2. Print numbers directly
print(100)
print(3.14)

# 3. Print multiple items together
print("My age is", 20)
print("I love", "Python", "programming")

# 4. Print using variables
name = "Priyanshu"
age = 20
print("Name:", name)
print("Age:", age)

# 5. Print formatted strings (f-strings)
print(f"My name is {name} and I am {age} years old.")

# 6. Print with custom separators
print("apple", "banana", "cherry", sep=" | ")

# 7. Print without moving to a new line
print("Loading", end="...")
print("Done!")  # continues on the same line

# 8. Print special characters
print("Line1\nLine2\nLine3")  # \n = new line
print("Tab\tSpace")           # \t = tab space

# 9. Print mathematical expressions
print("Sum of 10 and 5 is:", 10 + 5)
print("Square of 4 is:", 4 ** 2)

